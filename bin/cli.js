#!/usr/bin/env node
var reporter = require('../lib/reporter'),
  minimist = require('minimist');

var argv = minimist(process.argv.slice(2));

if (!argv.jsonFile || !argv.output) {
  console.log(`
Usage: node cli.js --jsonFile=<FILE> --output=<File> [options]

Options:

\t--jsonFile\tAbsolute or relative path to the json file generated by cucumber before
\t--output\tAbsolute or relative path to the html file generate with this tool
\t--theme\t\tdefault: 'bootstrap'

For more details please visit https://github.com/gkushang/cucumber-html-reporter

All options are supported by the cli
`);
  return;
}
argv.theme = argv.theme || 'bootstrap';

reporter.generate(argv);
